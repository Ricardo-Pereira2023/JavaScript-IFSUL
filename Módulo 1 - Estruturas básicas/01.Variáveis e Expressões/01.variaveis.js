// Atribuir diversos valores em uma mesma variável.

var y

y = 37 // valor inteiro (int)
document.write(y)
document.write("<br>")

y = "Ricardo Pereira" // valor string (texto)
document.write(y)
document.write("<br>")

y = true // valor lógico (booleano)
document.write(y)
document.write("<br>")

y = null // variável nula
document.write(y)
document.write("<br>")